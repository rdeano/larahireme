    {
        $controller = 'category';

        $categoryinfo = DB::table('category')
                        ->where('id',$id)
                        ->get();


        $categorylist = DB::table('users')
                        ->where('category_id',$id)
                        ->get();


        return view('category.index')
          ->with('categoryinfo',$categoryinfo)
          ->with('controller',$controller)
          ->with('categorylist',$categorylist);
    }

